# int to float
num_int = 10 
print('num_int:', num_int)	
num_float = float(num_int)
print('num_float:', num_float)

# float to int
gravity = 3.14
print(int(gravity))

# int to str
num_int = 10
print(num_int)
num_str = str(num_int)
print(num_str)

# str to int or float
num_str = '10'
print(int(num_str))
print(float(num_str))

# str to list
first_name = 'Chumirann'
print(first_name)
first_name_to_list = list(first_name)
print(first_name_to_list)
